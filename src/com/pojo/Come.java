public class Come {
	private String comeName;
	private String comeEmail;
	private String comePhone;
	private Integer comeId;
	private String introduce;
	public Integer getComeId() {
		return comeId;
	}
	public void setComeId(Integer comeId) {
		this.comeId = comeId;
	}
	public String getComeName() {
		return comeName;
	}
	public void setComeName(String comeName) {
		this.comeName = comeName;
	}
	public String getComeEmail() {
		return comeEmail;
	}
	public void setComeEmail(String comeEmail) {
		this.comeEmail = comeEmail;
	}
	public String getcomePhone() {
		return comePhone;
	}
	public void setcomePhone( String comePhone) {
		this.comePhone = comePhone;
	}
	public String getIntroduce() {
		return introduce;
	}
	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}
}
