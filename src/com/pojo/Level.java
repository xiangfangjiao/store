package com.pojo;

public class Level {
		private Integer level_Id;
		private String levelMessage;
		private String levelRequest;
		public Integer getLevel_Id() {
			return level_Id;
		}

		public void setLevel_Id(Integer level_Id) {
			this.level_Id = level_Id;
		}

		public String getLevelMessage() {
			return levelMessage;
		}

		public void setLevelMessage(String levelMessage) {
			this.levelMessage = levelMessage;
		}

		public String getLevelRequest() {
			return levelRequest;
		}

		public void setLevelRequest(String levelRequest) {
			this.levelRequest = levelRequest;
		}

		
		
}
